    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try {
        // Retrieve the initial price and discounts
        double initialPrice = Double.parseDouble(jTextField1.getText());
        int additionalDiscount = Integer.parseInt((String) jComboBox1.getSelectedItem());
        int memberBonus = Integer.parseInt((String) jComboBox2.getSelectedItem());
        int sliderDiscount = jSlider1.getValue();

        // Calculate the total discount percentage
        int totalDiscount = additionalDiscount + memberBonus + sliderDiscount;

        // Calculate final price and amount saved
        double discountAmount = initialPrice * totalDiscount / 100.0;
        double finalPrice = initialPrice - discountAmount;

        // Display final price and savings
        jLabel6.setText("Harga Akhir: Rp" + String.format("%.2f", finalPrice));
        jLabel7.setText("Hemat: Rp" + String.format("%.2f", discountAmount));

        // Append to history
        String historyEntry = "Harga Awal: Rp" + initialPrice + ", Diskon: " + totalDiscount + "%, Harga Akhir: Rp" + finalPrice;
        historyList.add(historyEntry);
        historyTextArea.append(historyEntry + "\n");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan harga awal yang valid", "Input Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

     private void updateHistoryTextArea() {
        StringBuilder historyText = new StringBuilder();
        for (String riwayat : historyList) {
            historyText.append(riwayat).append("\n");
        }
        historyTextArea.setText(historyText.toString());
    }
    
    
